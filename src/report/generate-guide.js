function run() {
  console.log('Generating ESPN-API-GUIDE.md...\n');

  const sections = [
    { name: 'Header',           fn: buildHeader },
    { name: 'Authentication',   fn: buildAuth },
    { name: 'Base Endpoints',   fn: buildEndpoints },
    { name: 'View Parameters',  fn: buildViews },
    { name: 'X-Fantasy-Filter', fn: buildFilters },
    { name: 'Response Schemas', fn: buildSchemas },
    { name: 'Historical Data',  fn: buildHistory },
    { name: 'Quick Reference',  fn: buildQuickReference },
  ];

  const parts = [];
  for (const { name, fn } of sections) {
    process.stdout.write(`  ${name.padEnd(22)}`);
    try {
      const content = fn();
      parts.push(content);
      console.log('✓');
    } catch (err) {
      console.log(`✗ (${err.message})`);
      parts.push(`## ${name}\n\n*Error generating section: ${err.message}*\n\n`);
    }
  }

  const guide = parts.join('\n---\n\n');
  writeFileSync(outPath, guide);

  const lines = guide.split('\n').length;
  const kb = Math.round(Buffer.byteLength(guide, 'utf8') / 1024);
  console.log(`\nWrote ESPN-API-GUIDE.md — ${lines} lines, ${kb} KB`);
  console.log(`Path: ${outPath}`);
}

run();
