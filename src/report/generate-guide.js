/**
 * generate-guide.js
 *
 * Reads all results/ JSON files produced by the discovery scripts and
 * synthesizes a comprehensive ESPN-API-GUIDE.md at the project root.
 *
 * Sections:
 *   1. Header + run metadata
 *   2. Authentication
 *   3. Base endpoints (from endpoints-interesting.json)
 *   4. View parameters (baseline + discovered views + schemas)
 *   5. X-Fantasy-Filter (discovered keys + value probes)
 *   6. Response schemas (per-view field listings)
 *   7. Historical data (boundaries, per-season degradation)
 *   8. Quick reference (copy-paste curl + JS snippets)
 *
 * Each section guards against missing files — you can run a subset of
 * discovery scripts and still generate a partial guide.
 *
 * Usage:  node src/report/generate-guide.js
 *    or:  npm run guide
 */

import { readFileSync, writeFileSync, existsSync, readdirSync, statSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import config from '../config.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..', '..');
const resultsDir = join(root, 'results');
const outPath = join(root, 'ESPN-API-GUIDE.md');

// ── Helpers ───────────────────────────────────────────────────────────────────

function readResult(filename) {
  const fp = join(resultsDir, filename);
  if (!existsSync(fp)) return null;
  try {
    return JSON.parse(readFileSync(fp, 'utf8'));
  } catch {
    return null;
  }
}

function lastModified(filename) {
  const fp = join(resultsDir, filename);
  if (!existsSync(fp)) return null;
  return statSync(fp).mtime.toISOString().slice(0, 16).replace('T', ' ');
}

/** Pad a markdown table row */
function row(...cells) {
  return `| ${cells.join(' | ')} |`;
}

/** Markdown table header + divider */
function tableHeader(...cols) {
  return [
    row(...cols),
    row(...cols.map(() => '---')),
  ].join('\n');
}

/** Format a JS fetch snippet for a given URL + options */
function fetchSnippet(url, { view, filter, description } = {}) {
  const lines = [];
  lines.push('```js');
  if (filter) {
    lines.push(`const filter = ${JSON.stringify(filter, null, 2)};`);
    lines.push('');
  }
  lines.push(`const res = await fetch('${url}', {`);
  lines.push(`  headers: {`);
  lines.push(`    'Accept': 'application/json',`);
  if (filter) lines.push(`    'X-Fantasy-Filter': JSON.stringify(filter),`);
  lines.push(`    // 'Cookie': 'espn_s2=...; SWID=...',  // add for private leagues`);
  lines.push(`  },`);
  lines.push(`});`);
  lines.push(`const data = await res.json();`);
  lines.push('```');
  return lines.join('\n');
}

function curlSnippet(url, { filter } = {}) {
  const lines = [`curl '${url}' \\`, `  -H 'Accept: application/json' \\`];
  if (filter) lines.push(`  -H 'X-Fantasy-Filter: ${JSON.stringify(filter)}' \\`);
  lines.push(`  # -H 'Cookie: espn_s2=...; SWID=...'  # add for private leagues`);
  return '```bash\n' + lines.join('\n') + '\n```';
}

