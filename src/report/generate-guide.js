  lines.push('> Re-run `node src/report/generate-guide.js` to refresh after running discovery scripts.');
  lines.push('');

  const meta = [];
  meta.push(`| Field | Value |`);
  meta.push(`| --- | --- |`);
  meta.push(`| Season | ${config.season} |`);
  meta.push(`| League ID | ${config.leagueId || '*(not set)*'} |`);
  meta.push(`| Auth | ${config.isAuthenticated ? '✓ Authenticated (espn_s2 + SWID set)' : '✗ Public only'} |`);
  meta.push(`| Generated | ${new Date().toISOString().slice(0, 16).replace('T', ' ')} UTC |`);

  const files = ['baseline-summary.json', 'views-summary.json', 'filters-summary.json',
    'endpoints-interesting.json', 'history-report.json', 'schema-index.json'];
